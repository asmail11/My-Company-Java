    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String strSearch = "select * from workon_data where ";
                if(rdoEmpNo.isSelected()){
                    strSearch += " employee_no " + getLike();
                } else if(rdoEmpName.isSelected()){
                    strSearch += " employee_name " + getLike();
                } else if(rdoProjNo.isSelected()){
                    strSearch += " project_no " + getLike();
                } else {
                    strSearch += " project_name " + getLike();
                }
                work.getCustomRows(strSearch, tblWorkON);
    }//GEN-LAST:event_btnSearchActionPerformed

