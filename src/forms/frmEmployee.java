    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
   
        String strName = txtEmpName.getText();
        String strAddress = txtAddress.getText();
        String strSalary = txtSalary.getText();
        
        if(strName.equals("")){
            Tools.msgBox("Employee Name Cant Be Empty");
        } else if(strAddress.equals("")){
            Tools.msgBox("Employee Address Cant Be Empty");
        } else if(strSalary.equals("")){
            Tools.msgBox("Employee Salary Cant Be Empty");
        } else if(hiringDate.getDate()==null){
            Tools.msgBox("Employee Hiring Date Cant Be Empty");
        } else if(birthDate.getDate()==null){
            Tools.msgBox("Employee Birth Date Cant Be Empty");
        } else if(jComboBox1.getSelectedItem() == null){
            Tools.msgBox("You Have To Choose Location");
        } else{
        setValues();
        emp.update();
        
        empPhone.setEmpNo(Integer.parseInt(txtEmpNo.getText()));
        empPhone.deleteByEmp();
        
        for(int i = 0; i < tablePhones.getRowCount(); i++){
            empPhone.setPhone(tablePhones.getValueAt(i, 0).toString());
            empPhone.add();
        }
        
        clearData();
        emp.getAllRows(tableEmployees);
        
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

