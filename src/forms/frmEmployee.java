    private void tableEmployeesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEmployeesMouseClicked
       try{
           int row = tableEmployees.getSelectedRow();
           txtEmpNo.setText(tableEmployees.getValueAt(row, 0).toString());
           txtEmpName.setText(tableEmployees.getValueAt(row, 1).toString());
           txtAddress.setText(tableEmployees.getValueAt(row, 2).toString());
           txtSalary.setText(tableEmployees.getValueAt(row, 3).toString());
           
           SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
           Date hDate = f.parse(tableEmployees.getValueAt(row, 4).toString());
           Date bDate = f.parse(tableEmployees.getValueAt(row, 5).toString());
           hiringDate.setDate(hDate);
           birthDate.setDate(bDate);
           
           jComboBox1.setSelectedItem(tableEmployees.getValueAt(row, 7).toString());
           empPhone.setEmpNo(Integer.parseInt(txtEmpNo.getText()));
           empPhone.getAllRows(tablePhones);
           
           btnAdd.setEnabled(false);
           btnUpdate.setEnabled(true);
           btnDelete.setEnabled(true);
           
       }catch(Exception ex){
           Tools.msgBox(ex.getMessage());
       }
    }//GEN-LAST:event_tableEmployeesMouseClicked

